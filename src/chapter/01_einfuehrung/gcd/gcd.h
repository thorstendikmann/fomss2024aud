#ifndef GCD_H
#define GCD_H

/**
 * "Brute Force" implementation of a GCD algorithm for two numbers g and k.
 * @param g number 1
 * @param k number 2
 */
int gcd_bf(int g, int k);

/**
 * Euklidian implementation of a GCD algorithm for two numbers g and k.
 * @param g number 1
 * @param k number 2
 */
int gcd_euklid(int g, int k);

/**
 * Modern Euklidian implementation of a GCD algorithm for two numbers g and k.
 * @param g number 1
 * @param k number 2
 */
int gcd_modern(int g, int k);

/**
 * Recursive Euklidian implementation of a GCD algorithm for two numbers g and k.
 * @param g number 1
 * @param k number 2
 */
int gcd_recursive(int g, int k);

#endif  // GCD_H
